		<p class="focus-theme">Key Themes</p>
		<div class="theme-upper">
			<div class="first">
				<div class="contain">
					<p class="numbering">01</p>
					<p class="title">Keynote Talk</p>
				</div>
				<p class="theme-details">Gives attending delegates the opportunity to hear from leading voices in the industry. These talks feature relevant topics, key issues and business opportunities aligned with the speaker’s experience and expertise, selected by the speaker in concert with the summit’s Executive Committee.</p>
			</div>
			<div class="second">
				<div class="contain">
						<p class="numbering">02</p>
						<p class="title">Thought Leadership </p>
				</div>
				<p class="theme-details">Led by an executive from the solution provider, Thought Leadership sessions provide comprehensive overviews of current business concerns, offering strategies and solutions for success. This is a unique opportunity to access the perspective of a leading member of the solution provider community.</p>
			</div>
		</div>
		<div class="theme-middle">
			<div class="first">
				<div class="contain">
						<p class="numbering">03</p>
						<p class="title">Executive Visions</p>
				</div>
				<p class="theme-details">Sessions are panel discussions that enable in-depth exchanges on critical business topics. Led by a moderator, these sessions encourage attending delegates to address industry challenges and gain valuable insight through interaction with expert panel members.</p>
			</div>
			<div class="second">
				<div class="contain">
						<p class="numbering">04</p>
						<p class="title">Round Table</p>
				</div>
				<p class="theme-details">Led by an industry analyst, expert or a member of the vendor community, Roundtables are open-forum sessions with strategic guidance. Attending delegates gather to collaborate on common issues and challenges within a format that allows them to get things done.</p>
			</div>
			<div class="third">
				<div class="contain">
						<p class="numbering">05</p>
						<p class="title">Executive  Exchange</p>
				</div>
				<p class="theme-details">Offer one-on-one interaction between executives and solution providers. This is an opportunity for both parties to make key business contacts, ask direct questions and get the answers they need. Session content is prearranged and based on mutual interest.</p>
			</div>
		</div>
		<div class="theme-lower">
			<div class="first">
				<div class="contain">
						<p class="numbering">06</p>
						<p class="title">Focus Group</p>
				</div>
				<p class="theme-details">Focus Groups allow executives to discuss business drivers within particular industry areas. These sessions allow attendees to isolate specific issues and work through them. Presentations last 15-20 minutes and are followed by Q&A sessions.</p>
			</div>
			<div class="second">
				<div class="contain">
						<p class="numbering">07</p>
						<p class="title">Networking Session</p>
				</div>
				<p class="theme-details">Take various unique forms, merging enjoyable, relaxing and productive activities with an environment conducive to in-depth conversation. These gatherings allow attendees to wind down between sessions and one-on-one interactions, while still furthering discussions and being productive.</p>
			</div>
